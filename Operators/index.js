// Operators ( + , - ,* ,/ , < ,> , % ...)

// 1. Arithematic Operator (+ , - ,* ,/, % )
const a = 10;
const b = 20;
let sum = a + b;

// 2. Assignment Operator ( = )
const c = 20;

// 3. Logical Operator ( && , || , !)
